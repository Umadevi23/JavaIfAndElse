package love.programming;

public class ElseAndIf {
    public static void main(String[] args) {
        int x =20;
        int y =20;
        if( x < y)
            System.out.println("x is less than y");
        else if (x == y)
            System.out.println("x is equal to y");
        else if(x > y)
            System.out.println("x is greater than y");

    }
}
